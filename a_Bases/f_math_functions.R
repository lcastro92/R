#Las funciones matemáticas que tenemos son las siguientes:
#max() devuelve el máximo de un set
max(11,20,-2)

#min() devuelve el mínimo de un set
min(11,20,-2)

#sqrt() devuelve la raíz cuadrada de un número (sólo número positivos, sino obviamente da error)
sqrt(64)

#abs() devuelve el valor absoluto de un número
abs(-13.5)

#ceiling() devuelve el entero superior más cercano al valor ingresado
ceiling(4.3)

#floor() devuelve el entero inferior más cercano al valor ingresado
floor(4.3)




